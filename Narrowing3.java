class Narrowing3{
	public static void main(String [] args){
		long a = 464613159910l;
		System.out.println(a);
		
		double b = a;
		System.out.println(b);

		float c = a;
		System.out.println(c);

		char d = (char)a;
		System.out.println(d);
		
		int e = (int)a;
		System.out.println(e);

		short s = (short)a;
		System.out.println(s);

		byte g = (byte)a;
		System.out.println(g);		
		

		
		
	}
}