import java.util.Scanner;

class DoWhile{
	public static void main(String [] args){
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int i = 0;                                                                                                     
		do
		{
			System.out.println("Hello From Gaurav");
			i++;
		}
		while(i<n);
	}
}