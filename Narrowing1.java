class Narrowing1{
	public static void main(String [] args){
		double a = 1234.3442;
		System.out.println(a);
		
		float b = (float)a;
		System.out.println(b);

		char c = (char)a;
		System.out.println(c);

		long d = (long)a;
		System.out.println(d);
		
		int e = (int)a;
		System.out.println(e);

		short s = (short)a;
		System.out.println(s);

		byte g = (byte)a;
		System.out.println(g);		
		

		
		
	}
}